
.ORIG x3000
		LD R1, A		;load the values of A, B, and C
		LD R2, B		;stored in x3102, x3103, x3104
		LD R3, C
		LD R4, D		
		LD R5, E
		LD R6, #5
		JSR TOTAL
		JSR DIV			;DIV SUBROUTINE
		STI R1, T
		STI R0, QUOT
		STI R7, R
		HALT

A		.FILL x3101
B		.FILL x3102
C		.FILL x3103
D		.FILL x3104
E		.FILL x3105
T		.FILL x3106
QUOT		.FILL x3107
R		.FILL x3108

QUIT2		NOT R3, R3		;IN NEGATIVE AFTER THEY MULTIPLY
		ADD R3, R3, #1

TOTAL		ADD R1, R1, R2
		ADD R1, R1, R3
		ADD R1, R1, R4
		ADD R1, R1, R5
	
DIV		ADD R6, R6, #0		;CHECKS IF Y IS 0
		BRz QUIT3
		;ADD R6, R2, #0		;5 INTO R6
		AND R5, R5, #0		;CLEAR R5
		ADD R3, R1, #0		;X INTO R3
		ADD R4, R6, #0		;5 INTO R4
		NOT R4, R4		;INVERT
		ADD R4, R4, #1		;2S COMPLEMENT FOR SUBTRACTION
		ADD R0, R3, R4		;SUBTRACT X BY 5
		BRn QUIT2		;CHECKS IF 5 > X

LOOP3		ADD R5, R5, #1		;COUNTER
		ADD R3, R3, R4		;SUBTRACT X BY 5
		BRp LOOP3		;IF POS GO BACK TO LOOP3
		BRz QUIT3		;IF O GO TO QUIT3
		ADD R3, R3, R6		;IF NEG ADD THE RESULT +5
		ADD R5, R5, #-1		;IF NEG ADD THE COUNTER +1

BR		QUIT3

QUIT3		RET			;BACK TO CALLING PROGRAM

.END
